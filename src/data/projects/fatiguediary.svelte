<p>
    Fatigue Diary is a web app for people suffering from chronic fatigue (due to ME/CFS, Long COVID,
    MS, EBV, ...). Fatigue patients often keep track of their energy level on a paper chart to plan
    their day and schedule enough rest time. The goal of this project was to find out whether I can
    create a web app that makes it more comfortable to use than the chart on paper. My web app
    allows users to enter their energy level after every activity and to display different charts
    showing the energy level throughout the day, week, month, or year. Additionally, the web app
    rates the activities based on their impact on the energy level which is calculated using all the
    user‚Äôs entered data from the past month.
    <br />
    The web app was tested by about 15 therapists who were referred to me by my therapist and about as
    many patients who found my web app on social media. The few feedbacks I received from therapists
    and patients, as well as my own experience show that the web app offers a more efficient and practical
    way of tracking one‚Äôs energy.
</p>
