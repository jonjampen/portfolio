<h2>Description</h2>
<p>
    During the third year of my matura I took a computer science class where we programmed a
    visualization of the mandelbrot set using JavaScript and the p5.js library. We learned about
    complex numbers as well as the basics of object oriented programming. Then the task was to
    create our own visualization of the mandelbrot set. I added features like:
</p>
<ul>
    <li>Zoom</li>
    <li>Rectangle Zoom (hold shift and drag)</li>
    <li>Customize Colors</li>
    <li>Edit mathematical parameters</li>
    <li>Move canvas</li>
    <li>Set canvas size</li>
    <li>Download a picture</li>
</ul>
<h2>What I learned</h2>
<p>
    After completing this class I learned about the basics of object oriented programming and the
    concept of complex numbers.
</p>
